def suma(a,b):
    resultado = a + b   
    return resultado
def resta(a,b):
    resultado = a - b   
    return resultado
def multiplicar(a,b):
    resultado = a * b   
    return resultado
def dividir(a,b):
    resultado = a / b   
    return resultado
def potencia(a,b):
    resultado = a ** b   
    return resultado       

print("Suma: ", suma(5,6))
print("Resta: ", resta(5,6))
print("Multiplicacion: ", multiplicar(5,6))
print("Division: ", dividir(5,6)) 
print("Potencia: ", potencia(5,6))